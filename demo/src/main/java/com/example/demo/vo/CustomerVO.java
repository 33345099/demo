package com.example.demo.vo;

public class CustomerVO {
	
	private String id;
	private String name;
	private String number;
	private String address;
	private String contcator;
	private String contcatPhone;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getNumber() {
		return number;
	}
	public void setNumber(String number) {
		this.number = number;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getContcator() {
		return contcator;
	}
	public void setContcator(String contcator) {
		this.contcator = contcator;
	}
	public String getContcatPhone() {
		return contcatPhone;
	}
	public void setContcatPhone(String contcatPhone) {
		this.contcatPhone = contcatPhone;
	}
	

}
